package Day02;

public class Day02_1 {

	public static void main(String[] args) {
		
		// ; : 실행 단위 구분
		// 동일한 변수 명 사용 불가
		
		int value = 10; 
		// 변수 선언과 초기화 / int : 약 +- 20억 / value [아무거나] : 이름 설정 / = : 오른쪽 데이터가 왼쪽으로 대입[저장]
		int result = value + 10;
		// result[아무거나] = 변수 호출 + 10
		System.out.println(result);
	}
}
